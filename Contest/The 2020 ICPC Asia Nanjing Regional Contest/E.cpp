#include <bits/stdc++.h>
using namespace std;
#define impo return puts("Impossible"),void()
int mx,my;
char s[200050];
int L,R,U,D;
string ans;

void solve()
{
	ans.resize(0);
	scanf("%d%d",&mx,&my);
	scanf("%s",s+1);
	L=R=U=D=0;
	int n=strlen(s+1);
	for(int i=1;i<=n;++i)
	{
		if(s[i]=='L')L++;
		if(s[i]=='R')R++;
		if(s[i]=='U')U++;
		if(s[i]=='D')D++;
	}
	if(R-L==mx&&U-D==my)return puts("Impossible"),void();
	if(mx==0&&my==0)return puts("Impossible"),void();
	if(L==0&&R==0&&mx==0)
	{
		if(U-D>=my&&my>0)return puts("Impossible"),void();
		if(U-D<=my&&my<0)impo;
	}
	if(U==0&&D==0&&my==0)
	{
		if(R-L>=mx&&mx>0)return puts("Impossible"),void();
		if(R-L<=mx&&mx<0)impo;
	}
	if(mx==0)
	{
		if(L>R)
		{
			ans+='L';
			L--;
			while(U--)
			{
				ans+='U';
			}
			while(D--)
			{
				ans+='D';
			}
			while(L--)
			{
				ans+='L';
			}
			while(R--)
			{
				ans+='R';
			}
		}
		else
		{
			if(R>0)
			{
				ans+='R';
				R--;
			}
			if(my<0)
			{
				while(U--)
				{
					ans+='U';
				}
				while(D--)
				{
					ans+='D';
				}
			}
			else
			{
				while(D--)
				{
					ans+='D';
				}
				while(U--)
				{
					ans+='U';
				}
			}
			while(R--)
			{
				ans+='R';
			}
			while(L--)
			{
				ans+='L';
			}
		}
	}
	else if(my==0)
	{
		if(U>D)
		{
			ans+='U';
			U--;
			while(L--)
			{
				ans+='L';
			}
			while(R--)
			{
				ans+='R';
			}
			while(U--)
			{
				ans+='U';
			}
			while(D--)
			{
				ans+='D';
			}
		}
		else
		{
			if(D>0)
			{
				ans+='D';
				D--;
			}
			if(mx<0)
			{
				while(R--)
				{
					ans+='R';
				}
				while(L--)
				{
					ans+='L';
				}
			}
			else
			{
				while(L--)
				{
					ans+='L';
				}
				while(R--)
				{
					ans+='R';
				}
			}
			while(D--)
			{
				ans+='D';
			}
			while(U--)
			{
				ans+='U';
			}
		}
	}
	else
	{
		if(R-L==mx)
		{
			while(U--)
			{
				ans+='U';
			}
			while(D--)
			{
				ans+='D';
			}
			while(L--)
			{
				ans+='L';
			}
			while(R--)
			{
				ans+='R';
			}
		}
		else
		{
			while(L--)
			{
				ans+='L';
			}
			while(R--)
			{
				ans+='R';
			}
			while(U--)
			{
				ans+='U';
			}
			while(D--)
			{
				ans+='D';
			}
		}
	}
	cout<<ans<<'\n';
}

int main()
{
	int Test;
	scanf("%d",&Test);
	while(Test--)solve();
	return 0;
}
